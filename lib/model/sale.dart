
class Sale {
  final int id;
  final String customerName;
  final String productName;
  final String phone;
  final int qty;
  final double amount;
  final double total;

  Sale({
    required this.id,
    required this.customerName,
    required this.productName,
    required this.phone,
    required this.qty,
    required this.amount,
    required this.total,
  });
}
